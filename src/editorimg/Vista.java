    private void cmbEfectoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbEfectoActionPerformed
          efectoSeleccionado = String.valueOf(cmbEfecto.getSelectedItem());
                if (imagenOriginal != null) {
        
        if (hiloProcesamiento != null && hiloProcesamiento.isAlive()) {
            hiloProcesamiento.interrupt();
        }

        hiloProcesamiento = new HiloProcesamientoImagen(imagenOriginal, efectoSeleccionado, this);
        hiloProcesamiento.start();
    }
    }//GEN-LAST:event_cmbEfectoActionPerformed

