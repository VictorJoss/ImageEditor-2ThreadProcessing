    private void btnAddImgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddImgMouseClicked
        
        JFileChooser fileChooser = new JFileChooser();
                int result = fileChooser.showOpenDialog(Vista.this);
                if (result == JFileChooser.APPROVE_OPTION) {
                    File selectedFile = fileChooser.getSelectedFile();
                    try {
                        BufferedImage imagen = ImageIO.read(selectedFile);
                        imagenOriginal = new BufferedImage(imagen.getWidth(), imagen.getHeight(), BufferedImage.TYPE_INT_RGB);
                        Graphics g = imagenOriginal.getGraphics();
                        g.drawImage(imagen, 0, 0, null);
                        g.dispose();

                        panelImagen.setImagen(imagenOriginal);
                        
                        //imagenProcesada = imagenOriginal;
                        
           CardLayout layout = (CardLayout) jPanel1.getLayout();
           layout.show(jPanel1, "card3");  
           
                    } catch (IOException ex) {
                        ex.printStackTrace();
                    }
                }
  
    }//GEN-LAST:event_btnAddImgMouseClicked

